package Pojo;

public class getResponse {
	private String url;
	private String services;
	private String expertise;
	private getCourses courses;
	private String intructor;
	private String linkedIn;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getServices() {
		return services;
	}
	public void setServices(String services) {
		this.services = services;
	}
	public String getExpertise() {
		return expertise;
	}
	public void setExpertise(String expertise) {
		this.expertise = expertise;
	}
	public Pojo.getCourses getCourses() {
		return courses;
	}
	public void setCourses(Pojo.getCourses courses) {
		this.courses = courses;
	}
	public String getIntructor() {
		return intructor;
	}
	public void setIntructor(String intructor) {
		this.intructor = intructor;
	}
	public String getLinkedIn() {
		return linkedIn;
	}
	public void setLinkedIn(String linkedIn) {
		this.linkedIn = linkedIn;
	}
	
	

}
